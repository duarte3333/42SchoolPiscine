#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	a1;
	int	a2;

	if (b != 0)
	{
		a1 = (*a / *b);
		a2 = (*a % *b);
		*a = a1;
		*b = a2;
	}
}
